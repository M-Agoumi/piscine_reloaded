int	ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);
	if (nb == 4)
		return (2);
	i = 2;
	while (i++ < nb / 2)
	{
		if (i * i == nb)
			return (i);
	}
	return (0);
}
